import mongoose from 'mongoose';
const Schema = mongoose.Schema;

const postSchema = Schema({
    title: String,
    message: String,
    creator: String,
    tags: [String],
    selectedFile: String,
    likeCount: {
        type: Number,
        default: 0,
    },
    createdAt: {
        type: Date,
        default: new Date(),
    },
})

var PostMessage = mongoose.model('Memories', postSchema);

export default PostMessage;